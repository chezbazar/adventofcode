fun List<Long>.group() = List(listSize) {index -> this.count { it.toInt() == index }.toLong() }

fun List<Long>.generation(steps: Int): List<Long> = if (steps == 0) this else this.mapIndexed { index, _ -> this[(index + 1) % this.size] + if (index == 6) this[0] else 0 }.generation(steps - 1)